CREATE PROCEDURE sp_update_DB_Size_Tracking
AS
INSERT INTO DB_Size_Tracking (DB_ID, DB_NAME, Size)
SELECT databases.database_id AS [DB_ID], databases.name AS [DB_Name], CONVERT(VARCHAR, sum(size)*8/1024) AS [Current Size - MB] 
FROM sys.databases 
JOIN sys.master_files ON databases.database_id=master_files.database_id 
GROUP BY sys.databases.database_id, sys.databases.name;




